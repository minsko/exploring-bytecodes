package com.mattinsko.math;

public class MyMath {

public int add(int i, int j) {
	return i+j;
}

public long div(long i, long j) {
	return i/j;
}

public int mod(int i, int j) {
	return i%j;
}

public double mult(double d, double e) {
	return d * e;
}

public float times2(float f) {
	return f*2;
}
}